// aplikasi sederhana javascript operator logika

// operator AND &&
let nama = 'farhan';
let password = 'Farhan@4123';

if(nama == 'farhan' && password == 'Farhan@4123'){
    console.log("Login berhasil");
}else{
    console.log("login gagal");
}

// operator OR ||
if(nama == 'farhan' || password == 'Farhan@412'){
    console.log("Login berhasil");
}else{
    console.log("login gagal");
}